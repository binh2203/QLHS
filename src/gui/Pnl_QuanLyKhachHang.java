    public String tangMaKH() {
        int i = tableModel_KhachHang.getRowCount();
        System.out.println(i-1);
        String maKH = (String) tblQuanLyKHACHHANG.getValueAt(i-1, 1);
        maKH = maKH.split("KH")[1].trim();
        int num = Integer.parseInt(maKH)+1;
        if(num<10){
            maKH = "KH00"+num;
        }else{
            maKH = "KH0"+num;
        }
        return maKH;
                
    }
    
