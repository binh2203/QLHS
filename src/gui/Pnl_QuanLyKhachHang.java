    	String day = sdf.format(dcNgaySinh.getDate());
    	String date[] = day.split("-");
    	int nam = Integer.parseInt(date[0]);
    	int thang = Integer.parseInt(date[1]);
    	int ngay = Integer.parseInt(date[2]);
